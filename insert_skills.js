let done = 0, skip = 0, miss = 0;
for (const [rel, block] of Object.entries(blocks)) {
  const file = path.join(ROOT, rel);
  if (!fs.existsSync(file)) { console.log('缺失文件:', rel); miss++; continue; }
  const lines = fs.readFileSync(file, 'utf8').split('\n');
  if (lines.some((l) => l.trim() === '## 业务场景 Skill')) { skip++; continue; }
  const idx = lines.findIndex((l) => /^## /.test(l));
  if (idx < 0) { console.log('未找到 ## 标题:', rel); miss++; continue; }
  lines.splice(idx, 0, block, '');   // 插入 block，再插入一个空行，原标题顺延
  atomicWrite(file, lines.join('\n'));
  done++;
}
console.log(`插入 ${done} 个，跳过(已存在) ${skip} 个，异常 ${miss} 个，共计 ${Object.keys(blocks).length} 条目`);
